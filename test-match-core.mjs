  // 주택목록(매입형)은 개별값 없어 range 겹침 폴백 유지
  ok(/맞음/.test(areaOf(Pa(40, 50), areaReq([], { 전용면적: { 최소: 30, 최대: 45 } }))), '#16 매입형 주택목록 range 겹침 → 맞음');
