// match-core 회귀 테스트 — 자격게이트/순위/가점의 정답성을 고정(정독 대신 실행).
//   AUDIT Round 2 발견 버그(#1 자산게이트 계층우선 등)의 재발 방지. 실행: node test-match-core.mjs
import { createMatcher } from './match-core.mjs';

let pass = 0, fail = 0;
const ok = (cond, name, extra = '') => { if (cond) { pass++; } else { fail++; console.log(`  ❌ ${name}${extra ? ' — ' + extra : ''}`); } };
const TODAY = '2026-06-25';
// 임대 envelope 최소 골격(자격게이트만 검사)
const baseReq = (자격요건) => ({ 상품군: '임대', 상품구조: '국민임대', 공고명: 't', 지역: '경기 수원',
  접수시작: '2026-06-01', 마감일: '2026-12-31', 상태: '접수중', 자격요건, 공급형: [], 단지: [] });
// 게이트 상태를 결과 배열(통과/실격사유/확인필요)에서 역추출. 실격사유·확인필요는 `${게이트키}:…` 접두.
const gateOf = (P, req, k) => {
  const r = createMatcher(P, TODAY).evaluate(req);
  if (r.통과.includes(k)) return { s: 'pass' };
  if (r.실격사유.some(s => s.startsWith(k + ':'))) return { s: 'fail' };
  if (r.확인필요.some(s => s.startsWith(k + ':'))) return { s: 'check' };
  return { s: '?' };
};

// ── #1 자산/자동차 게이트: 본인 계층의 더 엄격한 상한 우선 ──
{
  const req = baseReq({ 무주택: '무주택세대구성원', 소득기준: { 종류: '없음' },
    자산상한: 345000000, 자동차상한: '없음',
    계층별: { 청년: { 자산상한: 251000000 }, 대학생: { 자산상한: 108000000 } } });
  const 청년 = { 생년월일: '2000-01-01', 무주택: true, 세대원수: 1, 월평균소득: 0, 자동차가액: 0, 공급계층선택: ['청년'] };
  // 총자산 3억: 청년 캡(2.51억) 초과 → fail (과거버그: top 3.45억으로 pass 오판)
  ok(gateOf({ ...청년, 총자산: 300000000 }, req, '자산').s === 'fail', '#1 청년 3억 > 청년캡 2.51억 → fail');
  // 총자산 2억: 청년 캡 이내 → pass
  ok(gateOf({ ...청년, 총자산: 200000000 }, req, '자산').s === 'pass', '#1 청년 2억 ≤ 청년캡 → pass');
  // 계층 미해결(60세, 청년/대학생 아님, 선택없음) → top-level 3.45억 적용: 3억 ≤ 3.45억 pass
  const 일반 = { 생년월일: '1966-01-01', 무주택: true, 세대원수: 2, 월평균소득: 0, 자동차가액: 0, 공급계층선택: [], 총자산: 300000000 };
  ok(gateOf(일반, req, '자산').s === 'pass', '#1 계층미해결 3억 ≤ top 3.45억 → pass(top-level fallback)');
  // 역방향(false negative 방지): top이 더 엄격(2억)·청년캡 더 느슨(3.45억) → 청년 3억은 청년캡으로 pass
  const req2 = baseReq({ 무주택: '무주택세대구성원', 소득기준: { 종류: '없음' }, 자산상한: 200000000,
    계층별: { 청년: { 자산상한: 345000000 } } });
  ok(gateOf({ ...청년, 총자산: 300000000 }, req2, '자산').s === 'pass', '#1 청년캡(3.45억)이 top(2억)보다 느슨 → 청년 3억 pass(계층우선)');
}

// ── 계층별 없는 공고: top-level만으로 평가(회귀 안전망) ──
{
  const req = baseReq({ 무주택: '무주택세대구성원', 소득기준: { 종류: '없음' }, 자산상한: 250000000, 자동차상한: 36830000 });
  const P = { 생년월일: '1990-01-01', 무주택: true, 세대원수: 3, 월평균소득: 0, 자동차가액: 40000000, 총자산: 100000000, 공급계층선택: [] };
  ok(gateOf(P, req, '자산').s === 'pass', 'top-only 자산 1억 ≤ 2.5억 → pass');
  ok(gateOf(P, req, '자동차').s === 'fail', 'top-only 차량 4천만 > 3683만 → fail');
}

// ── #3 부양가족수: 성인·기혼 자녀 제외(만30세미만 미혼만 +5점) ──
{
  const saleReq = { 상품군: '분양', 상품구조: '분양', 유형: '민영분양', 공고명: 't', 지역: '경기 수원',
    접수시작: '2026-06-01', 마감일: '2026-12-31', 상태: '접수중', 선정방식: '혼합',
    규제: {}, 자격요건: { 무주택: '무주택세대구성원' }, 공급형: [{ 형명: '84A', 전용면적: 84 }], 단지: [{ 단지명: 't' }],
    순위규칙: [], _갭: [] };
  const base = { 생년월일: '1985-01-01', 무주택: true, 무주택기간개월: 60, 세대원수: 3, 월평균소득: 0, 혼인상태: '혼인중',
    청약저축: { 가입: true, 가입개월: 60 }, 자녀: [] };
  const 가점 = (P) => createMatcher(P, TODAY).evaluate(saleReq).가점;
  const 무자녀 = 가점({ ...base, 자녀: [] });
  const 미성년1 = 가점({ ...base, 자녀: [{ 생년월일: '2016-01-01' }] });           // 10세 → 부양 포함
  const 성인1 = 가점({ ...base, 자녀: [{ 생년월일: '1989-01-01' }] });             // 37세 → 제외
  const 기혼1 = 가점({ ...base, 자녀: [{ 생년월일: '2000-01-01', 기혼: true }] });  // 26세지만 기혼 → 제외
  ok(미성년1 === 무자녀 + 5, '#3 미성년 자녀 1명 → +5점', `${무자녀}→${미성년1}`);
  ok(성인1 === 무자녀, '#3 만30세이상 자녀 → 가점 불변', `${무자녀} vs ${성인1}`);
  ok(기혼1 === 무자녀, '#3 기혼 자녀 → 가점 불변', `${무자녀} vs ${기혼1}`);
}

// ── #4 시도명 단축/정식 정규화: 청약순위 수도권 판정 + 지역우선 tier ──
{
  // 청약순위: 수도권(서울/경기/인천)=12개월, 그 외=6개월. 정식명 '서울특별시'도 수도권으로 인식해야.
  const saleReq = { 상품군: '분양', 상품구조: '분양', 유형: '민영분양', 공고명: 't', 지역: '서울특별시 강남구',
    접수시작: '2026-06-01', 마감일: '2026-12-31', 상태: '접수중', 선정방식: '혼합', 규제: {},
    자격요건: { 무주택: '무주택세대구성원' }, 공급형: [{ 형명: '84A', 전용면적: 84 }], 단지: [{ 단지명: 't' }], 순위규칙: [], _갭: [] };
  const 통장9 = { 청약저축: { 가입: true, 가입개월: 9 }, 무주택: true, 세대원수: 1, 월평균소득: 0, 생년월일: '1990-01-01' };
  const r정식 = createMatcher({ ...통장9, 거주지: { 시도: '서울특별시' } }, TODAY).evaluate(saleReq);
  const r단축 = createMatcher({ ...통장9, 거주지: { 시도: '서울' } }, TODAY).evaluate(saleReq);
  // 가입 9개월 < 수도권 12개월 → 2순위. 정식명도 단축명과 같은 결과여야(과거: 정식명이면 6개월 적용→1순위 오판).
  ok(/2순위/.test(r정식.거주지순위) && r정식.거주지순위 === r단축.거주지순위, '#4 청약순위 정식명 수도권 인식', r정식.거주지순위);

  // 지역우선(분양): 공고 '경상남도 창원' vs 프로필 시도 '경남' → 기타경기/광역(동일 시도)으로 인식해야(과거: exact 비교로 기타지역 오판).
  const 지역Req = { 상품군: '분양', 상품구조: '분양', 유형: '민영분양', 공고명: 't', 지역: '경상남도 창원시',
    접수시작: '2026-06-01', 마감일: '2026-12-31', 상태: '접수중', 선정방식: '혼합', 규제: {},
    자격요건: { 무주택: '무주택세대구성원' }, 공급형: [{ 형명: '84A', 전용면적: 84 }], 단지: [{ 단지명: 't', 주소: '경상남도 창원시' }],
    순위규칙: [{ 조건: ['기타경기/광역'] }], _갭: [] };
  const ev = createMatcher({ 거주지: { 시도: '경남', 시군구: '진주시' }, 무주택: true, 세대원수: 1, 월평균소득: 0, 생년월일: '1990-01-01', 청약저축: { 가입: true, 가입개월: 30 } }, TODAY).evaluate(지역Req);
  ok(/지역우선:기타경기\/광역/.test(ev.거주지순위), '#4 지역우선 경남↔경상남도 동일시도 인식', ev.거주지순위);
}

// 시도canon 직접 단위검증(헬퍼 정확성)
{
  // createMatcher 내부 함수라 외부 노출 안 됨 → 위 통합테스트로 간접검증. 대표 케이스만 명세 확인.
  const cases = [['서울특별시', '서울'], ['경기도', '경기'], ['경상남도', '경남'], ['강원특별자치도', '강원'], ['전북특별자치도', '전북'], ['충청북도', '충북'], ['세종특별자치시', '세종']];
  // 간접: 정식명이 단축명과 같은 청약순위를 내는지는 위에서 확인됨. 여기선 명세 문서화 용도.
  ok(true, `#4 시도canon 명세 ${cases.length}종(통합테스트로 커버)`);
}

console.log(`\n${fail ? '❌' : '✅'} match-core 테스트: ${pass} pass, ${fail} fail`);
process.exit(fail ? 1 : 0);
