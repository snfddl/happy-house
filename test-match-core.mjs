  ok(areaOf(Pa(40, 50), areaReq([])) === '면적정보없음', '#16 면적정보 없는 공고 → 면적정보없음');
  ok(areaOf(Pa(null, null), areaReq([59, 84])) === null, '#16 희망 미입력 → 면적 판정 표시 안 함(null)');
